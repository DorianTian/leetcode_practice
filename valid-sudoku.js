/**
 * @param {string[][]} board
 * @return {boolean}
 */
let isValidSudoku = function(board) {
  const rows = Array.from({ length: 9 }, () => Array(9).fill(false));
  const cols = Array.from({ length: 9 }, () => Array(9).fill(false));
  const boxes = Array.from({ length: 9 }, () => Array(9).fill(false));

  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      let num = board[i][j];

      if (num === '.') continue;

      const n = num - '1';
      const boxIndex = Math.floor(i / 3) * 3 + Math.floor((j / 3));

      if (rows[i][n] || cols[j][n] || boxes[boxIndex][n]) {
        return false;
      }

      rows[i][n] = true;
      cols[j][n] = true;
      boxes[boxIndex][n] = true;

    }
  }

  return true;
};

